package org.example.dto;

import org.hibernate.validator.constraints.br.CPF;

import java.time.LocalDate;

public class ClienteDto {

    // cliente
    private Long cliId;
    private String cliNome;
    @CPF
    private String cliCpf;
    private LocalDate cliDataNascimento; // novo campo

    // endereco
    private String endRua;
    private String endNumero;
    private String endBairro;
    private String endCidade;
    private String endCep;
    private String endEstado;

    // contato
    private String conCelular;
    private String conEmail;

    public ClienteDto() {
    }

    public Long getCliId() {
        return cliId;
    }

    public void setCliId(Long cliId) {
        this.cliId = cliId;
    }

    public String getCliNome() {
        return cliNome;
    }

    public void setCliNome(String cliNome) {
        this.cliNome = cliNome;
    }

    public String getCliCpf() {
        return cliCpf;
    }

    public void setCliCpf(String cliCpf) {
        this.cliCpf = cliCpf;
    }

    public LocalDate getCliDataNascimento() {
        return cliDataNascimento;
    }

    public void setCliDataNascimento(LocalDate cliDataNascimento) {
        this.cliDataNascimento = cliDataNascimento;
    }

    public String getEndRua() {
        return endRua;
    }

    public void setEndRua(String endRua) {
        this.endRua = endRua;
    }

    public String getEndNumero() {
        return endNumero;
    }

    public void setEndNumero(String endNumero) {
        this.endNumero = endNumero;
    }

    public String getEndBairro() {
        return endBairro;
    }

    public void setEndBairro(String endBairro) {
        this.endBairro = endBairro;
    }

    public String getEndCidade() {
        return endCidade;
    }

    public void setEndCidade(String endCidade) {
        this.endCidade = endCidade;
    }

    public String getEndCep() {
        return endCep;
    }

    public void setEndCep(String endCep) {
        this.endCep = endCep;
    }

    public String getEndEstado() {
        return endEstado;
    }

    public void setEndEstado(String endEstado) {
        this.endEstado = endEstado;
    }

    public String getConCelular() {
        return conCelular;
    }

    public void setConCelular(String conCelular) {
        this.conCelular = conCelular;
    }

    public String getConEmail() {
        return conEmail;
    }

    public void setConEmail(String conEmail) {
        this.conEmail = conEmail;
    }
}